
public class javadoc {//Nombre de la clase-Inicio de programa

	
private String nombre;//Nombre de cada persona
private int edad;//Edad de cada persona

public void setNombre(String nom) {//Establecemos el nombre de la persona
	nombre=nom;//Declaramos que nombre es lo mismo que nom
}
public String getNombre() {//Obtenemos el nombre de la persona
	return nombre;//Cierre
}
public void setEdad(int ed) {//Establecemos la edad de una persona
	edad=ed;//Declaramos que edad es igual a ed
}
public int getEdad() {//Obtenemos la edad de la persona
	return edad;//Cierre
}
	}//Cierre de programa


